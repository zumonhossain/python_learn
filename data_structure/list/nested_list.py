fruits = [
    ["Apple", "Banana", "Cherry", "Strawberry"],
    ["Mango", "Orange", "Peach", "Watermelon"],
    ["Grapes", "Pineapple", "Kiwi", "Blueberry"]
]
print(fruits)  # Output: [['Apple', 'Banana', 'Cherry', 'Strawberry'], ['Mango', 'Orange', 'Peach', 'Watermelon'], ['Grapes', 'Pineapple', 'Kiwi', 'Blueberry']]
print(fruits[0])  # Output: ['Apple', 'Banana', 'Cherry', 'Strawberry']
print(fruits[0][1])  # Output: Banana
print(fruits[1][3])  # Output: Watermelon
print(fruits[2][0])  # Output: Grapes
print(fruits[2][3])  # Output: Blueberry
